
public class switch_case {
    public static void main(String[] args) {
        int deg = 2;
        switch (deg) {
            case 1 :
                System.out.println("bugün pazartesi");
                break;
            case 2 :
                System.out.println("bugün salı");
                break;

            default :
                System.out.println("Hatalı giriş yaptınız.");

        }

    }

}
